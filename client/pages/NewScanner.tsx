const formatVal = (val: any): string => {
  if (val === undefined || val === null) return "-";
  const num = Number(val);
  if (isNaN(num)) return String(val);
  return Number.isInteger(num) ? num.toString() : parseFloat(num.toFixed(3)).toString();
};

// Same pastel background per symbol as Scanner page (cosmetic only)
const getColor = (symbol: string) => {
  const colors = [
    "#e3f2fd", "#fce4ec", "#e8f5e9", "#fff3e0", "#ede7f6", "#f3e5f5",
    "#e0f7fa", "#f1f8e9", "#fffde7", "#fbe9e7", "#edeef7", "#e6f3f8",
  ];
  const hash = symbol.split("").reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return colors[hash % colors.length];
};

