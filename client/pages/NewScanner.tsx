// Same date formatting as Scanner page popup
const formatDate = (timestamp: string) => {
  if (!timestamp) return "N/A";
  let date = new Date(timestamp);
  if (isNaN(date.getTime())) {
    // Backend sends en-IN locale strings like "16/6/2026, 11:46:08 am"
    // (DD/M/YYYY), which the Date constructor can't parse directly.
    const match = timestamp.match(
      /^(\d{1,2})\/(\d{1,2})\/(\d{4}),?\s*(\d{1,2}):(\d{2}):(\d{2})\s*(am|pm)$/i,
    );
    if (match) {
      const [, day, month, year, hour, minute, second, meridiem] = match;
      let h = parseInt(hour, 10) % 12;
      if (meridiem.toLowerCase() === "pm") h += 12;
      date = new Date(
        parseInt(year, 10),
        parseInt(month, 10) - 1,
        parseInt(day, 10),
        h,
        parseInt(minute, 10),
        parseInt(second, 10),
      );
    }
  }
  if (isNaN(date.getTime())) return timestamp;
  return date.toLocaleString("en-GB", {
    day: "2-digit",
    month: "2-digit",
    year: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    hour12: true,
  });
};

